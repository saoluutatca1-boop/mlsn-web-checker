interface PixelAvatarProps {
  username: string
  size?: number
  className?: string
}

export const PixelAvatar: React.FC<PixelAvatarProps> = ({ username, size = 28, className = "" }) => {
  // Hash the username to get an index between 0 and 49 deterministically
  const hashIndex = React.useMemo(() => {
    const name = username ? username.trim() : "guest"
    let hash = 0
    for (let i = 0; i < name.length; i++) {
      hash = name.charCodeAt(i) + ((hash << 5) - hash)
    }
    return Math.abs(hash) % AVATAR_TEMPLATES.length
  }, [username])

  const template = AVATAR_TEMPLATES[hashIndex]

  return (
    <svg 
      width={size} 
      height={size} 
      viewBox="0 0 12 12" 
      shapeRendering="crispEdges"
      className={`bg-slate-950 border border-slate-900 rounded-lg overflow-hidden shrink-0 ${className}`}
    >
      <title>{`Pixel Avatar for ${username || 'guest'}`}</title>
      {template.map((row, y) => {
        const cleanRow = row.length > 12 ? row.slice(0, 12) : row
        return cleanRow.split('').map((char, x) => {
          if (char === '1') {
            return (
              <rect 
                key={`${x}-${y}`} 
                x={x} 
                y={y} 
                width={1} 
                height={1} 
                fill="#ffffff" 
              />
            )
          }
          return null
        })
      })}
    </svg>
  )
}
